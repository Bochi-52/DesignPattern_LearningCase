/**
 * 饮料制作系统，制作饮料的步骤大致相同（如烧水、冲泡、倒入杯子、加调料），但具体的冲泡和加调料步骤可能因饮料类型（如咖啡、茶）而不同。
 * 使用 模板方法模式 可以将公共步骤放在父类中，而将具体步骤的实现延迟到子类中。
 * 
 * 适用场景:
 * 固定流程：当某个操作的流程固定，但某些步骤的具体实现不同时
 * 代码复用：当多个类有相似的逻辑，但某些步骤需要自定义时
 * 功能扩展：当需要在不改变算法结构的情况下扩展某些步骤时
 */

/** 导入饮料类 */
const { Coffee, Tea } = require('./Beverage');


// 使用示例

// 制作咖啡
const coffee = new Coffee();
console.log("制作咖啡:");
coffee.prepare();
// 输出:
// 烧水
// 冲泡咖啡粉
// 倒入杯子
// 加糖和牛奶

// 制作茶
const tea = new Tea();
console.log("\n制作茶:");
tea.prepare();
// 输出:
// 烧水
// 浸泡茶叶
// 倒入杯子
// 加柠檬